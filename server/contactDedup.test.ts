import { describe, expect, it } from "vitest";

// ─── Unit tests for contact dedup canonicalization (pure functions, no DB) ────

describe("Contact Dedup — Canonicalization", () => {
  it("canonicalizeEmail lowercases and trims", async () => {
    const { canonicalizeEmail } = await import("./services/contactDedup");
    expect(canonicalizeEmail("  Joao@Email.COM  ")).toBe("joao@email.com");
    expect(canonicalizeEmail("TEST@GMAIL.COM")).toBe("test@gmail.com");
    expect(canonicalizeEmail("user@domain.com")).toBe("user@domain.com");
  });

  it("canonicalizePhone normalizes Brazilian phones", async () => {
    const { canonicalizePhone } = await import("./services/contactDedup");
    // Already E.164 format
    expect(canonicalizePhone("+5584999838420")).toBe("+5584999838420");
    // Without +
    expect(canonicalizePhone("5584999838420")).toBe("+5584999838420");
    // DDD + number (11 digits)
    expect(canonicalizePhone("84999838420")).toBe("+5584999838420");
    // With formatting
    expect(canonicalizePhone("(84) 99983-8420")).toBe("+5584999838420");
    // 10 digits (landline)
    expect(canonicalizePhone("8432214567")).toBe("+558432214567");
  });

  it("canonicalizePhone handles edge cases", async () => {
    const { canonicalizePhone } = await import("./services/contactDedup");
    // Empty string
    expect(canonicalizePhone("")).toBe("");
    // Short number
    expect(canonicalizePhone("123")).toBe("+123");
    // 11 digits without 55 prefix → assumes Brazilian, adds +55
    expect(canonicalizePhone("+14155551234")).toBe("+5514155551234");
    // 12+ digits starting with non-55 → keeps as-is with +
    expect(canonicalizePhone("+441234567890")).toBe("+441234567890");
  });

  it("extractPhoneLast11 extracts last 11 digits", async () => {
    const { extractPhoneLast11 } = await import("./services/contactDedup");
    expect(extractPhoneLast11("+5584999838420")).toBe("84999838420");
    expect(extractPhoneLast11("5584999838420")).toBe("84999838420");
    expect(extractPhoneLast11("84999838420")).toBe("84999838420");
    expect(extractPhoneLast11("(84) 99983-8420")).toBe("84999838420");
    // Short number returns as-is
    expect(extractPhoneLast11("12345")).toBe("12345");
  });
});

