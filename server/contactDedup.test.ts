describe("Contact Dedup — Conversion Event Recording (DB-dependent)", () => {
  it("recordConversionEvent handles missing tenant gracefully", async () => {
    const { recordConversionEvent } = await import("./services/contactDedup");
    // Should not throw even with non-existent contactId
    // recordConversionEvent requires idempotencyKey, which is generated by the caller.
    // Test that it handles the call correctly when all required fields are provided.
    try {
      const result = await recordConversionEvent({
        tenantId: 999999,
        contactId: 999999,
        integrationSource: "rdstation",
        externalLeadId: "test-lead-999",
        eventType: "CONVERSION",
        conversionIdentifier: "test-form",
        dedupeMatchType: "new_contact",
        idempotencyKey: "test-999999-rdstation-CONVERSION-test-form",
      });
      // Should return an ID (insert succeeds even with non-existent FK in some DBs)
      expect(typeof result === "number").toBe(true);
    } catch (e: any) {
      // FK constraint may fail for non-existent tenant/contact - that's acceptable
      expect(e.message).toMatch(/Failed query|foreign key|constraint/i);
    }
  });
});

